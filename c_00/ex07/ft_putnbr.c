#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	x;

	if (nb < 10)
	{
		x = nb + '0';
		write(1, &x, 1);
	}
	else
	{
		ft_putnbr(nb / 10);
		x = (nb % 10) + '0';
		write(1, &x, 1);
	}
}
