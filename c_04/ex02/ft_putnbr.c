#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	x;

	if (nb == -2147483648)
	{
		write(1, "-21", 3);
		nb = 47483648;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	if (nb < 10)
	{
		x = nb + '0';
		write(1, &x, 1);
	}
	else
	{
		ft_putnbr(nb / 10);
		x = (nb % 10) + '0';
		write(1, &x, 1);
	}
}
